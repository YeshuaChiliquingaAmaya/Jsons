    private void readData() throws NumberFormatException{
        int id;
        String name;
        int age;
        float weight;
        float height;
        String cellphone;
        String diseaseSymptoms;
        String systemicDiseases;
        String patientTreatment;
        Date treatmentDateStart;
        Date treatmentEndDate;
        
        id = Integer.parseInt(txtId.getText());
        name = txtFullName.getText();
        age = Integer.parseInt(txtAge.getText());
        weight = Float.parseFloat(txtWeigth.getText());
        height = Float.parseFloat(txtHeight.getText());
        cellphone = txtCellphone.getText();
        diseaseSymptoms = txtareaDiseaseSymptoms.getText();
        systemicDiseases = txtSystemicDiseases.getText();
        patientTreatment = txtTreatment.getText();
        
        treatmentDateStart = JcalendarStart.getDate();
        treatmentEndDate = jCalendarFinal.getDate();
        
        clinical = new ClinicalHistory(id, name, age, weight, height, diseaseSymptoms, cellphone, systemicDiseases, patientTreatment, treatmentDateStart, treatmentEndDate);
        
    }
    
