    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //instanciar un objeto del tipo LoginController con parametros que recogen lo que se escriba en la caja de texto
        LoginController login = new LoginController(txtUsername, txtPassword);
        login.PasswordController();
        
        //Verificar el usuario y la contrase침a
        boolean validUseaname = login.VerificateUsername();
        boolean validPassword = login.VerificatePassword();
        
        if (validUseaname && validPassword){
            FrmOdontoApp app = new FrmOdontoApp();
            JOptionPane.showMessageDialog(rootPane, "Bienvenida/o al sistema");
            app.setVisible(true);
            this.setVisible(false);
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

